using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BasicMathLab
{
    public class BasicMath
    {
        public double AddMethod(double a,double b)
        {
            double result = a + b;
            return result;
        }
        public double SubstractMethod(double a,double b)
        {
            double result = a - b;
            return result;
        }
        public double MultiplyMethod(double a , double b)
        {
            double result = a * b;
            return result;
        }
        public double DivideMethod(double a,double b)
        {
            double result = a / b;
            return result;
        }

    }
}
